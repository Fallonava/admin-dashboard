export function transformToPrismaSeeds() {
    const doctors = [];
    const shifts = [];
    let sId = 1;

    for (const doc of newDoctorsData) {
        doctors.push({
            id: doc.id,
            name: doc.name,
            specialty: doc.specialty,
            status: doc.status.replace(" ", "_").toUpperCase(),
            category: doc.category,
            queueCode: doc.queueCode,
            // Default placeholder initial values so Prisma is happy
            startTime: doc.shiftsRaw[0]?.time.split("-")[0] || "08:00",
            endTime: doc.shiftsRaw[0]?.time.split("-")[1] || "12:00",
            registrationTime: null,
            lastCall: null,
            lastManualOverride: null
        });

        for (const shift of doc.shiftsRaw) {
            for (const dayIdx of shift.days) {
                let title = doc.specialty;
                if (shift.title) title = title + " (" + shift.title + ")";

                // Append extra remarks dynamically to shift titles if available
                if (doc.remarks && !shift.title) {
                    // Keep title concise but provide details for edge cases
                }

                shifts.push({
                    dayIdx,
                    timeIdx: shift.title === "Sore" || shift.title === "Malam" ? 1 : 0,
                    title: title,
                    doctorId: doc.id,
                    color: doc.category === 'Bedah' ? 'blue' : 'emerald',
                    formattedTime: shift.time,
                    disabledDates: []
                });
            }
        }
    }

    return { doctors, shifts };
}
